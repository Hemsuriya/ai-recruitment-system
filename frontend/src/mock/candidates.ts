// ── Helper utilities ──────────────────────────────────────────────────
export function getScoreColor(score: number): string {
  if (score <= 0) return "#94A3B8";
  if (score >= 80) return "var(--score-high)";
  if (score >= 60) return "var(--score-mid)";
  return "var(--score-low)";
}

export function getVerdictColor(verdict: string): { color: string; bg: string } {
  switch (verdict) {
    case "Strong Hire": return { color: "var(--verdict-strong-hire)", bg: "var(--verdict-strong-hire-bg)" };
    case "Hire":        return { color: "var(--verdict-hire)",        bg: "var(--verdict-hire-bg)" };
    case "Maybe":       return { color: "var(--verdict-maybe)",       bg: "var(--verdict-maybe-bg)" };
    case "Reject":      return { color: "var(--verdict-reject)",      bg: "var(--verdict-reject-bg)" };
    default:            return { color: "#64748b",                    bg: "rgba(100,116,139,0.1)" };
  }
}

export function getStatusColor(status: string): { color: string; bg: string } {
  switch (status) {
    case "Final Review":       return { color: "var(--status-final)",    bg: "var(--status-final-bg)" };
    case "Interview Complete":  return { color: "var(--status-complete)", bg: "var(--status-complete-bg)" };
    case "Assessment Pending":  return { color: "var(--status-pending)",  bg: "var(--status-pending-bg)" };
    case "Screening":           return { color: "var(--status-screening)",bg: "var(--status-screening-bg)" };
    case "MCQ Complete":        return { color: "#7c3aed",               bg: "rgba(124,58,237,0.1)" };
    default:                    return { color: "#64748b",               bg: "rgba(100,116,139,0.1)" };
  }
}

